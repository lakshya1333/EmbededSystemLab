	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000 
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC1
	LDR R1,=SRC2
	LDR R5,=SUM
	LDR R8,=CARRY
    MOV R6,#4
UP  LDR R2,[R0],#4
    LDR R3,[R1],#4
	SUBS R4,R2,R3
	ADC R7, #0
	STR R4,[R5],#4
	SUB R6,#1
	TEQ R6,#0
	BNE UP
	SBC R7,#0
	STRB R7,[R8]
STOP B STOP
SRC1 DCD 0x12,0x11,0x20,0x34
SRC2 DCD 0x05,0x4,0x6,0x12
	AREA DATA, DATA, READWRITE
SUM DCD 0,0,0,0
CARRY DCB 0
	END