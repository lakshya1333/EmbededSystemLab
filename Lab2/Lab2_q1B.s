	AREA RESET,DATA,READONLY
	EXPORT 	__Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R0,#5
	LDR R1,=N+(SIZE-1)*4
	LDR R2,=N+(SIZE-1+SHIFT)*4
BACK LDR R3,[R1],#-4
	STR R3,[R2],#-4
	SUB R0,#1
	CMP R0,#0
	BNE BACK
STOP B STOP
SIZE equ 5
SHIFT equ 2
	AREA mydata,DATA,READWRITE
N DCD 0X1,0X2,0X3,0X4,0X5,0,0
	END