Reset_Handler
	MOV R3,#5
	LDR R0,=SRC
	LDR R1,=DSTN
BACK LDR R2,[R0],#4
	STR R2,[R1],#4
	SUB R3,#1
	CMP R3,#0
	BNE BACK
STOP B STOP
