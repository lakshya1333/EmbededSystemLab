	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=BCD    
	LDR R0,[R0]
	MOV R1,R0
	AND R0,#0XF
	LSR R1,#4
	MOV R2,0XA
	MLA R3,R1,R2,R0
	LDR R4,=HEX
	STR R3,[R4]
STOP B STOP
BCD DCD 0X15
	AREA mydata,DATA,READWRITE
HEX DCD 0
	END
	