Reset_Handler
	LDR R0,=HEX
	LDR R0,[R0]
	AND R2,R0,#0XF
	CMP R2,#9
	ADDHI R2,#0X37
	ADDLS R2,#0X30
	LSR R3,R0,#4
	CMP R3,#9
	ADDHI R3,#0X37
	ADDLS R3,#0X30
STOP B STOP
